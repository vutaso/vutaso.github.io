function switchLang(lang) {
  const dict = MB_I18N[lang];
  if (!dict) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
  document.documentElement.lang = lang;
  localStorage.setItem('vutaso-lang', lang);

  // Update typewriter phrases + re-render hero title immediately
  if (MB_TW[lang]) {
    window._twPhrases = MB_TW[lang];
    const el = document.getElementById('tw-el');
    if (el && el.textContent.trim()) {
      el.innerHTML = '<span class="gtext">' + MB_TW[lang][0] + '</span>';
    }
  }
}

// Auto-detect: saved preference → browser language → default vi
(function initLang() {
  const saved = localStorage.getItem('vutaso-lang');
  if (saved && (saved === 'vi' || saved === 'en' || saved === 'jp')) {
    switchLang(saved);
    return;
  }
  const browserLang = (navigator.language || navigator.userLanguage || '').toLowerCase();
  if (browserLang.startsWith('en')) { switchLang('en'); return; }
  if (browserLang.startsWith('ja')) { switchLang('jp'); return; }
  switchLang('vi');
})();
