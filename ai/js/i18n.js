  const getSystemPromptForMode = (mode, loc) => {
    const bucket = SYSTEM_PROMPT_PRESET_BUCKETS[mode];
    if (!bucket) return getDefaultSystemPrompt(loc);
    return bucket[normalizeLocale(loc || locale)] || bucket.en;
  };

